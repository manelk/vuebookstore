        </template>
        <template v-slot:item="{ item }">
          <tr>
            <td>
              <v-icon
                small
                class="mr-6"
                color="yellow darken-3
        "
                >mdi-account</v-icon
              >
              <span>{{ item.firstName }}</span>
            </td>
            <td>
              <span>{{ item.lastName }}</span>
            </td>
            <!-- The Profile Dialog -->
            <td class="center">
              <showContact :item="item"></showContact>
            </td>
